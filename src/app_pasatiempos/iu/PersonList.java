    public void listPersonsInTable(){
        
        for(int i=0; i < db.countPersons(); i++ ){
            Person p = db.getPersons().get(i);
            tablePersons.addRow(new Object[]{p.getDocument(),p.getName(), p.getLastName(), p.getGender()});
        }
        
    }


