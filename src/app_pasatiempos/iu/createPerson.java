    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        // TODO add your handling code here:
        String nameGender = "";
        int indexGender = gender.getSelectedIndex();
        if(indexGender == 0){
            nameGender = "Femenino";
        }else if(indexGender == 1){
            nameGender = "Masculino";
        }
        
        int idPerson = db.countPersons() + 1;
        
        Person p = new Person(idPerson,name.getText(), lastName.getText(),document.getText(),username.getText(),nameGender);
        db.addPerson(p);
        this.cleanText();
        this.setVisible(false);
        
        detail d = new detail(p);
        d.setVisible(true);
        d.setLocationRelativeTo(null);
    }//GEN-LAST:event_jButton1ActionPerformed

